// Comparison Operators: Write JavaScript code to compare two numbers and log whether they are equal or not to the console.

let num1 = Number(prompt("type in the first number for comparison"))
let num2 = Number(prompt("Type in the second number for comparison"))

if (num1 === num2) {
    console.log(`${num1} and ${num2} are the same`)
} else {
    console.log(`${num1} and ${num2} are not the same`)
}