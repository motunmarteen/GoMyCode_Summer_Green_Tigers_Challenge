// Logical or Boolean Operators: Write JavaScript code to check if a number is both greater than 10 and less than 20.

let num = Number(prompt("Enter a number to check : "))

if (num > 10 && num < 20) {
    console.log(`${num} is greater than 10 and less than 20`)
} else {
    console.log(`${num} is just there and nothing much`)
}